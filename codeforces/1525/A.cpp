const int MOD = 1e9 + 7;

int t;

int main(void) {
  scanf("%d", &t);
  while (t--) {
    int k;
    scanf("%d", &k);
    if (k == 0 || k == 100) {
      printf("1\n");
      continue;
    }
    int a = k, b = 100 - k;
    int g = __gcd(a, b);
    a /= g; b /= g;
    printf("%d\n", a + b);
  }
  return 0;
}
